import java.util.*;
public class forloop{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter the value of n:");
        int n = sc.nextInt();
        int i;
        for(i=1;i<=n;i++)
        {
            System.out.print("Hello world");
        }
    }
}